// Fetched from app.quicktype.io website due to JSON's complexity

public class Tags {
    private String item;
    private Object measure;
    private String quantity;
    private Object foodGroup;
    private long tagID;

    public String getItem() { return item; }
    public void setItem(String value) { this.item = value; }

    public Object getMeasure() { return measure; }
    public void setMeasure(Object value) { this.measure = value; }

    public String getQuantity() { return quantity; }
    public void setQuantity(String value) { this.quantity = value; }

    public Object getFoodGroup() { return foodGroup; }
    public void setFoodGroup(Object value) { this.foodGroup = value; }

    public long getTagID() { return tagID; }
    public void setTagID(long value) { this.tagID = value; }
}